<strong>Re-Authorization</strong>

<p><strong>Days of Availability</strong> - Enter how many days the Deliverable will be available for access.  Note that you should leave this box blank if there is no expiration of access.  (May be edited)
</p>
<p><strong>Access Re-Authorization Cycle</strong> - This is the frequency you wish AreteX to check the validity of your customer's authorization.  For example, a "Daily" re-authorization would have AreteX check every 24 hours.  If you choose "Monthly" here, AreteX will check for valid authorization every month.  
</p>
<p><em>Note</em>: "Single" in this instance means that AreteX will not check for a valid Authorization after the initial delivery.
</p>