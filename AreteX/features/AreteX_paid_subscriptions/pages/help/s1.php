<strong>New Membership Deliverable</strong>
<p>The Membership and Subscription deliverable integrates into the WordPress Roles &amp; Capablities.
</p>
<p>This wizard will create a paid membership deliverable.  It can be most useful for designing a building block to create a complex product.  The wizard will help you do such things as include time flexibility within your deliverable. 
</p>
<p>If you want to create a basic "membership only" product, you may find the wizard at <em>Setup/Wizards/Paid Membership</em> more convenient.  No matter which you use, you may edit any deliverable that has been created.
</p>
<p><strong>Membership Name</strong> - Write a name that is recognizable to you.
</p>
<p><strong>Description</strong> - This can help you provide more detail of the Deliverable.
</p>
<p><strong>Deliverable Code</strong> - This is a unique code for this specific Deliverable.  You may let AreteX assign one.</p>    