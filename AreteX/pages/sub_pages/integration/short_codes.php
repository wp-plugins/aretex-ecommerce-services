You may style the button as you need to match your site.
</p>
<p>To add a customized button or button image use this format: <span style="font-family: 'Courier New', Courier, monospace; font-size: 10pt; font-weight: bold;">[aretex_payee_signup]<em>Your Image or Button HTML</em>[/aretex_payee_signup]</span></p>
