        <h2>The [aretex_paid_content] Shortcode</h2>
        <p>With the <em>aretex_paid_content</em>  shortcode, you may charge for nearly <em>any</em> type of content you create on your WordPress site.  Only those users who have paid for it will be able to see it.
        Although this is a very flexible shortcode with a lot of options, in its simplest form all you have to do is wrap the shortcode around the content you wish to charge for:
        <br />
        <img src="<?php echo plugins_url('images/simple-pc.png',__FILE__); ?>" />
         </p>
        
        <p>This shortcode is used to <strong>display</strong> different content to your users depending on their <em>authorization status</em> for this deliverable.
        <strong>Note</strong>: <em>Authorization Status</em> in regards to <em>Paid Content</em> refers to a customer's right to access the deliverable.</p>
        
        <p>Although it is not strictly necessary for the customer to have a user account on your WordPress site to <em>purchase</em>  paid content, 
        they must be logged onto your site to <strong>view</strong> paid content.   If the customer purchases paid content <em><strong>without</strong> being logged on</em>, when they <strong><em>do</em> register and login</strong>,  
        the email address they provide on the payment form is matched to their WordPress email address to authenticate proper authorizaton to content.    
        
        </p>  
        <p>
        The exact <em>authorization status</em> of a given deliverable is determined for a specific user
        based on 1) the options you chose when you set up the deliverable, 2) the current payment status, and 3) 
        when the payment was made. 
        </p>
        <h4>Automatic Generation</h4>
