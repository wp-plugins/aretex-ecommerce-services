        <em>[aretex_paid_registration]</em> - This generates a simple front-end registration form for customers to sign up with.<br />
        <em>[aretex_register_button]</em> - This shortcode goes <em>inside</em> the aretex_paid_registration content and generates a "buy" button.<br />
        <em>[aretex_role_button]</em> - This shortcode generates a "buy" button for a membership upgrade.
        </p>
        <p>You should use these shortcodes to sell any <em><strong>Product</strong></em> with a <strong><em>Membership</em> deliverable</strong> in its <em><strong>Manifest</strong></em>. 
        That is because behind the scenes, these shortcodes work with the WordPress registration system as well as the WordPress Roles and Capablities system.        
        </p>
        <p>Paid Membership purchase shortcodes have two purposes: 1) <em>Registration</em> and 2) <em>"Upgrade" Payment</em>.  
        <em>Registration</em> allows non-members to sign up and pay for access to your site.  The role they are assigned when payment is complete depends on the product and the membership deliverable in the associated manifest.
        <em>Upgrade Payments</em> allow existing members to purchase a new role. 
        <em>Note:</em> It's a really good idea to only have <em>one</em> membership deliverable in any given manifest.  
        </p>
        <p>You <em><strong>may</strong></em> have other types of deliverables (i.e. <em>paid content</em>) in the product manifest when using <em>Paid Membership</em> purchase shortcodes. 
        AreteX&trade; will handle delivery authorization of other deliverables appropriately when it is bundled with a membership purchase. 
        (The converse is <em>not</em> true. Please do not use the <em>aretex_buy_now</em> shortcode for selling memberships.) </p>
