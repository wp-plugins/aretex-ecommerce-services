<h2>Payee Registration</h2>
<p>Whether a payee is a <em>Referrer</em>, a <em>Contributor</em> or both they will need to be registered on your site to access their <em>Payment Tracking and Reporting</em>  screen (PTR).</p>
<p>There are two ways to register payees: <em>Back End</em> and <em>Front End</em>. Regardless of which way they are assigned, 
they will be required to agree to both <em>your</em> terms of service and the AreteX&trade; terms of service before having full access to their PTR screen. (See <em>WP Integration/ PTR Integration</em> for more details.) </p>
<p>  
For <em>Back End</em> registration, see <em>Reports &amp; Management / Payees / Payee Management / Add</em>.
If your payee is already a member of your site, select their username in the Payee Managment screen when you Add or Edit their record.
</p>
<p>The rest of this tab addresses <em>Front End</em> payee registration. 
<em>Front End</em> payee registration allows people who are currently not members of your web site to sign up from a page on your site. </p>
<h4>The [aretex_payee_signup] Shortcode</h4>
<div class="section group"> <!-- ROW -->
    <div class="col span_7_of_12"> <!-- Column -->

<p>The <em>aretex_payee_signup</em> <strong>cannot</strong> be automatically generated by any wizard or from the AreteX&trade; editor button on the WordPress Page/Post editor.
When you put the shortcode <strong>[aretex_payee_signup]</strong> onto a WordPress Page or Post, it will generate a special registration form. 
When a potential payee signs up using this form, they are registered as a user on your  site with the role <em>AreteX Payee</em>.  
WordPress will send them their temporary password as is usual.  
When they sign on to their user dashboard, the <em>Payment Tracking and Reporting</em> (PTR) menu option will be available to them. See <em>WP Integration/ PTR Integration</em> for information on the PTR screen.</p>
<p>If you put the <em>[aretex_payee_signup]</em> shortcode on your page without any modifications, it will generate the default sign up form.</p>
<p>You may modify the payee sign up form by adding attributes and content. </p>

<h5>Attributes</h5> 
<p>The <strong>[aretex_payee_signup]</strong> shortcode supports the following attributes: <em>captcha</em>,  <em>commission_group</em> and <em>parent</em>. </p>
<strong><em>captcha</em></strong>
<p>The <strong><em>captcha</em></strong> attribute has three options: <em>None</em>, <em>visualCaptcha</em> (Default) and <em>visualCaptcha1</em> - These attribute values <em>are</em> case-sensative.
<ul style="text-indent: -1.0em; margin-left: 1.5em; font-size: 12px;">
<li><strong>None</strong> - No captcha is displayed.</li>
<li><strong>visualCaptcha</strong> (default) - This plugin uses a modified copy of <a href="http://visualcaptcha.net" target="_blank">Visual Captcha</a> version: 4.2.0 to provide additonal security to discourage automated sign ups for payment.
The user will be asked to drag and drop a specific image into the circle.
See the image of the <em>Default Payee Registration Form</em> for an example.  </li>
 <li><strong>visualCaptcha1</strong> This is an alternate arrangement of the visualCaptcha graphics. 
   
 </li>

</ul>
</p> 
<strong><em>commission_group</em></strong>
<p>The <em><strong>commission_group</strong></em> attribute is specifically for <em>referrer</em> registration. If the value of the <strong><em>commission_group</em></strong> 
attribute is a valid <strong>commission group code</strong> (See <em>Setup / Payouts / Commission Structures</em>) the new payee will automatically be assigned to that commission group upon successful registration.
<br /><em><strong>Example:</strong></em> <span style="font-family: 'Courier New', Courier, monospace; font-size: 10pt; font-weight: bold;">[aretex_payee_signup commission_group='AFF1']</span><br />
(This would assign the new registrant to the commission group with the code <em>AFF1</em>, if such a commission group exists in your AreteX&trade; database.)
 The default value of <em><strong>commission_group</strong></em> is <strong>None</strong>.
</p>
<strong><em>parent</em></strong>
<p>The <em><strong>parent</strong></em> attribute is specifically for <em>referrer</em> registration when you have a <em>commission_group</em>
with a <em>multi-tier commission structure</em>. The default value for this attribute is <strong>None</strong>.
</p>
<p>The attribute determines which payee will get the 2nd tier commission when this new referrer makes a sale. (That payee is known as the "Upline" payee, or "parent")</p>
<p>If the attribute value is <strong>tracking</strong>, then the upline payee is determined by the referral tracking code.  The person who referred the new registrant (either by URL or Coupon) 
will receive the 2nd Tier commission.
<br /><em><strong>Example:</strong></em> <span style="font-family: 'Courier New', Courier, monospace; font-size: 10pt; font-weight: bold;">[aretex_payee_signup parent="tracking" commission_group='AFF1']</span><br />
</p>
<p>If the attribute value is a valid <strong>WordPress User Name</strong> that user will be the upline payee. Of course, this requires that the specified WordPress user <em>also</em> be registered as a payee. 
<p>The specified payee will always receive the 2nd Tier commission. The following example assumes you have a WordPress user with the username <em>AffiliateManager</em>. 
<br /><em><strong>Example:</strong></em> <span style="font-family: 'Courier New', Courier, monospace; font-size: 10pt; font-weight: bold;">[aretex_payee_signup parent="AffiliateManager" commission_group='AFF1']</span><br />
</p>
<h5>Content</h5>
