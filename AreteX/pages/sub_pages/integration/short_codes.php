        <h5>Usage Details</h5>

        <div id="show_short_code_example">            

<p>The <em>[aretex_paid_content]</em> shortcode is of the following format:<br/>                        
<div style=" font-family: 'Courier New', Courier,  monospace; font-size:10pt; padding: 5px; "
class="ui-widget ui-widget-content  ui-corner-bottom">             
[aretex_paid_content deliverable_code="<strong>CODE</strong>" status="<strong>--SEE BELOW--</strong>"]
---- YOUR CONTENT APPROPRIATE TO STATUS ---
[/aretex_paid_content]
</div>
<p><em><strong>Important Note:</strong></em> Use "text mode" on the page editor when copying and pasting shortcodes to be sure
all embedded HTML is rendered properly.</p>
<p>Both the <em><strong>deliverable_code</strong></em> and <em><strong>status</strong></em> attributes are required. </p>
<p>Paid content is considered by AreteX&trade; to be a <em>deliverable</em>. As explained in <em>Catalog &amp; Products / Delivery Specifications </em> a deliverable can be part of any number of products via the product's <em>manifest</em>. 
        This allows you the flexiblity to bundle your content as you see fit. The <em>deliverable_code</em> lets AreteX&trade; 
        know which content deliverable you are putting on your Page or Post.  
        </p>
<p>Generally speaking, a user is considered <em>Authroized</em>  if they have a timely payment for <em>any product</em> with this <em>deliverable</em> in its <em>manifest</em>. </p>        
<p>The content <em>within</em> the shortcode an be <em>any</em> valid WordPress content, <em>including</em> other shortcodes.
That means you can embed audio, video, file download links, chatroom entrances or any other content you can put in WordPress.  
 </p>        

<p>The <em><strong>status</strong></em> attribute determines the conditions under which the content within the shortcode is displayed.
A full list of valid status condtions are listed below. </p>
        
