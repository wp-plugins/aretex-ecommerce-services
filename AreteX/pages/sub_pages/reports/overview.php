<h2>Reports and Management Overview</h2>

<p><strong><em>Reports and Management</em></strong> is the section/area within AreteX&trade; 
that allows you to view sales and delivery records, manage payee (contributor and referrer) records, 
and view payee transaction logs.
</p>
<p>You will be able to see the work you have done in the <em>Catalog and Products</em> section 
concerning <em>Deliverable</em> and <em>Product</em> definition.  It is included in the 
sales and payment reports.
</p>

<p><strong>Customers</strong> - This tab allows you to view your customer's account information, 
as well as to <em>cancel a rebill agreement</em>, if necessary. 
Unlike your customer's CAM (<em>Customer Account Manager</em>), these screens <strong>do not</strong> give you the ability to change the authorizing credit card or customer contact information.</p>
<p>
(<em>Note: </em> To turn on the ability of your <em>customers</em> to access their CAM, go to <em>WP Integration / Form Customization / Receipts and Customer Accounts / Welcome Account Access</em>.)
</p>
<p><strong>Sales Report</strong> - This tab is a searchable report of all sales.  
If desired, you can view individual <em>receipts</em>.  This is the tab that is used if you need to 
provide a <em>Refund</em> for a particular sale.
</p>
<p><strong>Deliveries</strong> - This tab provides access to two different reports: 
1) the <strong>Delivery Log</strong>, which is a report of all past delivery attempts, 
2) and <strong>Manage Deliveries</strong>, which is an editable report of all current and pending <em>delivery authorizations</em>.
</p>
<p><strong>Payout Report</strong> - This tab opens a report of all payouts you have made to contributors and referrers.
</p>
<p><strong>Payees</strong> - This tab gives you access to <strong>Payee Management</strong>, 
the <em>manual batch</em> payment feature, as well as the <strong>ACH/Direct Deposit</strong> 
transaction report.</p>