<h2>Deliveries Overview</h2>

<p>The <strong>Delivery Log</strong> is a report of all past delivery attempts. The far right columns provide details of the attempts. The listing can be sorted and filtered, using the <em>Search Box</em>, the column arrows, or the <em>Show/Hide Columns</em> button.
</p>
<p>The <strong>Manage Deliveries</strong> tab lists all current and pending delivery authorizations.  You may search or filter the report as above, as well as edit the individual records.
</p>
