<h2>Overview</h2>

<p>The <em>Payees</em> functionality tab/area provides you with the ability to manage your payee records (both contributors and referrers), manage manual payments to your payees, and to view reports on all payee transactions.
</p>
<p><strong>Payee Management</strong> - This screen allows you to see a general listing of all of 
your payees and to add a payee to your list.  The screen also includes <em>Action</em> buttons 
to <em>Edit</em>, <em>Delete</em>, see <em>pending</em> transactions for a payee, and a listing 
of payments <em>already sent</em> to a particular payee.  
(See <em>Payee Management</em> screen for more details.)
</p>
<p>If you need to adjust a payment because of a refund (<em>Sales Report / Refund icon)</em>,  select the "Pending Payment" icon of the appropirate Payee(s). If the payout has already occurred, select the "Add" button above the list inside 
         <em>Pending Payments</em>, otherwise adjust the appropriate pending payment.</p> </p>
<p><strong>Manual Batch</strong> - This screen gives you the ability to pay your contributors and 
referrers outside of the automatic ACH/Direct Deposit functionality of AreteX&trade;. This ability 
would be most important if an irregularity occurs with your or your payee's bank account of record, 
which would not allow Direct Deposit to be used.  This feature allows you to continue to get 
accurate payment due reports, as well as to mark payments you make for AreteX&trade; to provide 
follow-on reporting.  </p>
<p>
<em>Note</em>: For Manual Batch to work, an ACH authorization cannot be on file for that payee.  </p>
<p><strong>ACH Report</strong> - This screen gives a report of all ACH/Direct Deposit transactions.
</p>