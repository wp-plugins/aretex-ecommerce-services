<h2>CAM Overview</h2>

<p>The <strong>CAM</strong> (<em>Customer Account Manager</em>) is your customer's access to their account information including their billing and contact information, any rebill agreement information (including the ability to change credit card authorization), as well as payment/purchase history.</p>

<p>These management screens are provided for <em>you</em> to be able to view this information, as well 
as to <em>cancel a rebill agreement</em>, if necessary.  Unlike your customer's CAM, these screens do <strong>not</strong> give you the ability to 
change the authorizing credit card or customer contact information.</p>
