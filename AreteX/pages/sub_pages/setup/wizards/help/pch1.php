<strong>Glossary:</strong>
<br />
<p>
<strong>Product</strong> - This is the offering you wish to sell on your web site.  A product is made up of the deliverable(s) and manifest.
</p>
<p>
<strong>Deliverable</strong> - A Deliverable is an item or part of the full product or offering you are selling.  Each piece of the product is considered to be a deliverable.  For a simple product (like an on-line article) there may be only 1 deliverable per product offering.
</p>
<p>
<strong>Manifest</strong> - The Manifest is made up of all deliverables that make up your product.  For a simple product, the manifest will closely resemble or be identical to the Deliverable.
</p>
