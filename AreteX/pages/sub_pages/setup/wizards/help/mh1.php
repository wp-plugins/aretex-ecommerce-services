<strong>Paid Membership</strong>

<p>
<strong>Membership Name:</strong> Enter the name of the membership (product) you are offering.  Examples - Bronze Membership, Deluxe Membership, etc.
</p>
<p>
<strong>Describe the Membership:</strong> (optional) Briefly describe what this membership offers your customer.  There is a 255 character limit.
</p>
<p>
<strong>Product/Delivery Code:</strong>  This is the working code that will be used to identify your membership product.  The code has to be unique within AreteX.  With this wizard the code used for the product will also be used for the deliverable and manifest.  If you wish to have AreteX automatically assign your code, check the box provided.
</p>
<p>
Click "Next" to proceed to the next step in setting up your membership or subscription product.
</p>
<br />
<strong>Glossary:</strong>
<br />
<p>
<strong>Product</strong> - This is the access you wish to sell on your web site.  A membership or subscription product is made up of the deliverable(s) and manifest.
</p>
<p>
<strong>Deliverable</strong> - A Deliverable is an item or part of the full product.  Each piece of the product is considered to be a deliverable.  For a simple product (like on-line access only) there may be only 1 deliverable per product offering.
</p>
<p>
<strong>Manifest</strong> - The Manifest is made up of all deliverables that make up your product.  For a simple product, the manifest will closely resemble or be identical to the Deliverable.
</p>
