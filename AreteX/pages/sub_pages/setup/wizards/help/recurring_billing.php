<strong>Recurring Billing</strong>
<p>
<strong>Immediate Payment</strong> - This is the initial amount your customer will be charged.  If your immediate payment (charge) is free (0.00 or blank), be aware that some credit card issuers will require a minimum of a $1 authorization.  That may show up on your customers statement, but will not be charged.
</p>
<p>
<strong>Days Until Billing Starts</strong> - Enter the number of days you wait before the next charge on your customer's account for your product.
  Note that if you wish to have a monthly cycle, you can type in "30" into this box.  If weekly, type "7."  If your immediate payment is 0.00, the number in this box can reflect the number of days of a "free trial."
</p>
<p>
<strong>Billing Cycle</strong> - Choose the appropriate billing cycle you wish: monthly, weekly, daily, or yearly.
</p>
<p>
<strong>$ Billed per Cycle</strong> - This is the amount you will charge automatically in your billing cycle.
</p>
<p>
<strong>Total # of Payments</strong> - This is the total number of automatically billed payments.  (Note: this number does not include the immediate payment.)  If there is no time limit on the product you are offering (i.e., like a monthly newsletter) leave this box blank.  If this box is left blank, payments will continue until cancelled.
</p>
