<strong>Pricing Model</strong>

<p>There are three pricing models to choose from:</p>
<p>
<strong>Single Price</strong> - This is a 1-time offering in which your customers pay once to buy your product.
</p>
<p>
<strong>Recurring Billing</strong> - If you charge your customers on a cycle (like once per month), choose this model.
</p>
<p>
<strong>Donation</strong> - This model allows your donors to specify how much they will donate.  There is a mimum of $1 per donation.
</p>
<p>
Click "Next" to proceed to the next step.
</p>
<br />
<p>
<em>Note:</em> The Price Detail step will differ according to the pricing model you choose at this point.
</p>
