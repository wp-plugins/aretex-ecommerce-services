<p><strong>Glossary:</strong></p>

<p><strong>Role</strong> - Within WordPress, registered users are assigned capabilities based on their roles.  We use the same definition.
</p>
<p><strong>Registration </strong>- The process by which a person requests and thereby gains access to your site and/or materials by becoming a member on your site.
</p>