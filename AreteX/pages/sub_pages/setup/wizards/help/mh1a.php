<strong>Roles and Registration</strong>

<p><strong>Select Product Type</strong> - This product type choice distinguishes between members and non-members. To offer both types, be sure to create each product seperately.  (Within AreteX&trade; each product type is a deliverable.)
    <ul style="text-indent: -1.0em; margin-left: 1.5em; font-size: 12px;">
     <li><strong>Register with Role</strong> - This product definition is for signing up new members.  Shortcodes are generated to paste onto a page so you get a form with all the right registration options.  Upon payment confirmation the user will immediately be assigned the selected role.</li>  

     <li><strong>Role Only</strong> - This product definition is used when you wish to offer current members access to an upgraded or different role.  ("Role Only" does not have a registration, only a buy button.)</li>
    </ul>
</p>
<p>
<strong>Select Delivered Role </strong>- The options you see in the pulldown menu will include all the non-administrative roles in this WordPress installation.  (Note: To create roles, you can use any standard role management plugin.)  If you do not have multiple roles created, only the WordPress default role will be available.
</p>
<p>
<strong>Allow Free Registration</strong> - This option is available only with the "Register with Role" selection above.  Note that this choice disappears with the "Role Only" selection.
</p>
