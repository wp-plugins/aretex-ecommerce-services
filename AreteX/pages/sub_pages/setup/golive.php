<h2>Overview for "Go Live"</h2>

<p>The process of setting up live eCommerce functionality to your web site is a serious one - after all we are talking about how everyone's money is being handled, including your own. 
</p>
<p>Please follow the process that is outlined.  A flow chart is supplied below to give you a feel for the process and to know what to expect "next."  If you experience difficulty, please open a support ticket.
</p>

<p><strong><em>Note: </em></strong> Before you begin the process for going live with your AreteX 
License, be aware that any Sandbox payment related data will be erased, as they are not dealing with "real money". 
These include all sales records, delivery records,
customer records, payment records and payee records. If you wish to save these records you 
may download spreadsheets in each of the sub menus of <em>Reports &amp; Management.</em>
</p>
