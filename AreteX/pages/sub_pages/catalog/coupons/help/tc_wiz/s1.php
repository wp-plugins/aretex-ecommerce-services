<strong>Understanding Tracking Codes</strong>
<p>Why a Tracking Code?  A Tracking Code is the identifying string of code that AreteX uses to link a 
product price, a possible discount, the advertising medium, and payees together.  This allows for 
automatic payment splits, tracking different marketing efforts, as well as puts special incentives before 
your customers.</p>
<p>The purpose of this wizard is to walk you through the steps, as well as help you understand the 
various components of tracking codes.</p>
<p>Note: This wizard will generate a tracking code that you must copy and paste into your marketing
material or web site links.  Tracking Codes are generated "on the fly" from the components you have
previously defined or created, and are described in this wizard.</p>