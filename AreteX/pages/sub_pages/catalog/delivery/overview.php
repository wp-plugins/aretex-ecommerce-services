<h2>Delivery Overview</h2>
<p>Ensuring delivery of a sold product is a major concern for any business. AreteX&trade; eCommerce Services handles the delivery of digital access authorizations by building a line of accountability from the smallest piece or element up to the largest component.  
</p>
<p><strong>Deliverables</strong> are built from smaller parts into a saved and recognizable building block.  
</p>
<div style="margin-left: 15px;">
<p><strong>Paid Content Deliverables</strong> - Any individual piece of Product content for which you expect to receive payment. In this section you will be guided through creating your Deliverables, which includes attaching Contributor records to automatically make appropriate payments according to your agreements.
</p>
<p><strong>Paid Membership Deliverables</strong> - include items that are used for generating sales for memberships or subscriptions.  In this section you will be guided through creating your Deliverables, which also includes date and time access flexibility.
</p>
<p><em>Note</em>: A <em>WordPress Role</em> must be created before creating a <em>Membership Deliverable</em>.  See <em>WP Integration /  Roles and Capabilities</em>.
</p>
</div>
<p><strong>Manifests</strong> are the mechanism used to contain all the <em>Deliverables</em> needed to fulfill delivery of a <em>Product</em>.  It is also the place where the business owner may add or subtract Deliverables to improve Products.
</p>