<h2>Catalog and Products Overview</h2>

<p>The Catalog and Products tab is the area in which you build and edit product 
components, including the catalog specifics, the tracking and payout functionality, 
and identifying and linking delivery components.  The main thing to keep in mind as you 
put together your catalog is that products are built from many components, which you link 
together under a specific title and product code.
</p>
<p>
<em>Here is an example:</em>
<p>
<strong>General Product Information:</strong><br />
Say you are offering English 101 online.
<br />
The textbook is written by Sarah Deen, who will also be an instructor for you.  You have another instructor, Jim Dye.
</p>
<p>
<strong>Materials:</strong><br />
A deliverable is the text book and Sarah Deen will get a royalty for each textbook sold.  Another deliverable will be the syllabus from each instructor for their own section of English 101.
<br />
The manifest of the product English 101a will be - a textbook and the Sarah Deen syllabus.
<br />
The manifest for the product English 101b will be  - a textbook and the Jim Dye syllabus.
</p>
<p>
So the deliverables are used to build the manifest for each individual product.  (Look for this under the <em>Delivery Specifications</em> button.)
</p>

<p>
<strong>Contributor Payouts:</strong><br />
Sarah Deen will get a royalty from each textbook used, plus an instructor (contributor) fee for her class.<br />
Jim Dye will get an instructor (contributor) fee for his class.
</p>

<p><strong>Referrer Payouts:</strong><br />
If you have affiliates or commissioned sales reps that are presenting your course to others, 
these "Referrers" are put into a Referrer Group that allows them to get paid.  
The group may be associated with the product specifically or more broadly for more sales.  
However, the individuals are only associated with a Group for more broad scale commissioned payout setup.
  <ul style="text-indent: -1.0em; margin-left: 1.5em; font-size: 12px;">
  <li><strong>Setting up a Commision Group</strong> -  Go to <em>Setup/Payouts/Commission Structures</em>  to set up a Commission Group. </li>
  <li><strong>Adding Referrers To a Commission Group</strong> - Go to <em>Reports &amp; Management/Payees/Payee Management / Add (or Edit)</em>  to assign payees as Referrers to Commission Groups.</li>
  <li><strong>Signing Up Referrers</strong> - Under <em>WP Integration/Shortcodes &amp; Widget / Payee Registration </em> you will find instructions for using WordPress shortcodes to create sign up pages for your Referrers.</li>
  </ul>
</p>
<p>
<strong>Conclusion:</strong><br />
When all of your components are put together (see the <em>Add/Edit Products</em> button) you have a product that is complete for automating sales, tracking, payouts, delivery, and reporting.
</p>