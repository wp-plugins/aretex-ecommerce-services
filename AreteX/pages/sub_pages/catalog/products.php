<h2>Help and Information for Products</h2>
<p>
This tab helps you list your products within AreteX&trade; to offer them on your web site.  To do this in a way that fully integrates and automates your eCommerce products for sales, delivery, reports, and payouts the following information needs to be compiled into a product.
</p>
<p>
<strong>Products</strong> - There are two ways to create and list your products inside of AreteX&trade; - either use a simple product wizard inside the <em>Setup</em> tab and then edit here as you desire, or use the <em>Add/Edit</em> form here.  Any editing of a <em>Product</em> will be done in this tab.  
</p>
<p>
Inside of AreteX&trade; there are many things needed to help build your complete product for sale.  Each of the items like deliverables and manifests are built elsewhere, but are linked here for delivery.  The reason for this is that items like a deliverable may be part of one or several different products.  You will wish to associate appropriate deliverables with your products for accurate and effective delivery.
<ul>
<li><strong>Product Code</strong> - This is a unique identifier that AreteX&trade; uses.</li>
<li><strong>Product Name</strong> - This is any name you choose to show on your invoice.</li>
<li><strong>Notes &amp; Details</strong> - These notes are information you wish to insert into the receipt or invoice for better clarity of purchase, as well as meeting federal regulations and "best standards" practices for sales receipts.</li>
<li><strong>Pricing Model</strong> - This is the type of payment you will expect to receive from your customers.  Your options are single payment, recurring billing, and donation.  Note that under the donation model your benefactor may choose recurring donations, but it is your benefactor who may choose this option on the payment form. You may not choose recurring donations in the product setup.</li>
<li><strong>Manifest</strong> - This component contains the Deliverables, which are the items of content or access authorization being purchased. </li>
<li><strong>Contributor Payouts </strong>- This section allows you to specify any contributors (for royalties, etc.) to match the agreements you have with them.  If you already have your contributors identified with your deliverables and manifests, simply choose the Copy From Manifest button.  To edit the list or add someone, choose the Payouts button.</li>
</ul>
<p>Be sure to Save your work as you are making changes or additions.</p>  
<p>
<em>Note:</em> Adding the coupons and tracking information for such things as paying your commissioned or affiliate payees will be handled next under the Coupons and Tracking tab.
</p>
